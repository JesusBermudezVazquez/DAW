package challenges;

public class _01Chanllenge {

	public static void main(String[] args) {

		// DECLARE AND INITILIALIZD

		int num = 10;

		System.out.println("NUMBER FROM 10 TO 1 WILL BE DISPLAYED:");

		
		// USE DO WHILE TO DISPLAY THE FIRT VALUE OR THE VARIABLE

		do {

			System.out.print(num + " ");

			num--;
		} while (num >= 1);

		
	}

}
